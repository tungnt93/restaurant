Quy trình xây dựng thực đơn mới:
    -	Nhà bếp:
        •	Tên món ăn
        •	Phân loại
        •	Nguyên liệu
        •	Ước lượng giá
    -	Thêm món ăn vào thực đơn nhà hàng:
        •	Tên món ăn (chọn từ danh sách do bộ phận nhà bếp đã xây dựng)
        •	Phân loại
        •	Giá
        •	Giảm giá
        •	Ảnh minh họa.
Thực đơn hàng ngày
    -	Nhà bếp: Lên danh sách thực đơn cho ngày hôm sau, bao gồm:
        •	Tên món
        •	Số lượng
    -	Bộ phận mua hàng: Dựa vào danh sách do Nhà bếp đã xây dựng để mua thực phẩm, số lượng tùy theo số lượng còn lại trong kho.
Dụng cụ nhà bếp:
    -	List ra danh sách dụng cụ
    -	Thêm yêu cầu mua dụng cụ mới
